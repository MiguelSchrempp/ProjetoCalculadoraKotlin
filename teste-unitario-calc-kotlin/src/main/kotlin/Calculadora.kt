class Calculadora {

    fun soma(n1: Double, n2: Double): Any {
        return n1 + n2
    }

    fun subtrair(n1: Double, n2: Double): Any {
        return n1 - n2
    }

    fun multiplicar(n1: Double, n2: Double): Any {
        return n1 * n2
    }

    fun dividir(n1: Double, n2: Double): Any {
        return n1 / n2
    }
}